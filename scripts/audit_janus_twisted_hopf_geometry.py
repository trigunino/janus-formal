from __future__ import annotations

import json
import math
from dataclasses import asdict, dataclass


@dataclass(frozen=True)
class TwistedHopfAudit:
    alpha_squared_length_m: float
    uv_length_m: float
    logarithmic_tunnel_period: float
    quotient_contraction: float
    orientation_cover_period: float
    orientation_cover_contraction: float
    euler_characteristic: int
    ordinary_homology: str
    twisted_top_homology: str
    fundamental_group: str
    orientation_subgroup: str
    throat_topology: str
    orientation_double_cover: str
    geometric_deck_group: str
    pin_lift_group: str
    all_checks_pass: bool


def build_audit(
    alpha_squared_length_m: float = 1.0e26,
    uv_length_m: float = 1.616255e-35,
) -> TwistedHopfAudit:
    if alpha_squared_length_m <= 0.0 or uv_length_m <= 0.0:
        raise ValueError("lengths must be positive")

    period = math.log(2.0 * alpha_squared_length_m / uv_length_m)
    contraction = math.exp(-period)
    cover_period = 2.0 * period
    cover_contraction = contraction**2

    checks = [
        period > 0.0,
        0.0 < contraction < 1.0,
        math.isclose(
            contraction * 2.0 * alpha_squared_length_m,
            uv_length_m,
            rel_tol=1.0e-12,
        ),
        math.isclose(cover_contraction, contraction**2, rel_tol=1.0e-15),
        1 - 1 - 1 + 1 == 0,
    ]

    return TwistedHopfAudit(
        alpha_squared_length_m=alpha_squared_length_m,
        uv_length_m=uv_length_m,
        logarithmic_tunnel_period=period,
        quotient_contraction=contraction,
        orientation_cover_period=cover_period,
        orientation_cover_contraction=cover_contraction,
        euler_characteristic=0,
        ordinary_homology="H0=Z, H1=Z, H2=0, H3=Z/2, H4=0",
        twisted_top_homology="H4(J; Z_orientation)=Z",
        fundamental_group="Z generated by one PT-tunnel traversal",
        orientation_subgroup="2Z, giving the two-fold orientation cover",
        throat_topology="S2 x S1 with a once-twisted normal line",
        orientation_double_cover="S3 x S1",
        geometric_deck_group="Z2",
        pin_lift_group="Z4 when the lifted square equals fermion parity",
        all_checks_pass=all(checks),
    )


def main() -> int:
    audit = build_audit()
    print(json.dumps(asdict(audit), indent=2, sort_keys=True))
    return 0 if audit.all_checks_pass else 1


if __name__ == "__main__":
    raise SystemExit(main())
