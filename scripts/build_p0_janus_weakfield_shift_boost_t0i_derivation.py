from __future__ import annotations

from pathlib import Path
import json

import sympy as sp


REPORT_PATH = Path("outputs/reports/p0_janus_weakfield_shift_boost_t0i_derivation.md")
JSON_PATH = Path("outputs/reports/p0_janus_weakfield_shift_boost_t0i_derivation.json")
ETA = sp.diag(-1, 1, 1, 1)


def weakfield_shift_a_matrix() -> sp.Matrix:
    delta_phi, delta_psi, dbx, dby, dbz = sp.symbols("Delta_Phi Delta_Psi Delta_Bx Delta_By Delta_Bz")
    return sp.Matrix(
        [
            [-delta_phi, dbx, dby, dbz],
            [0, delta_psi, 0, 0],
            [0, 0, delta_psi, 0],
            [0, 0, 0, delta_psi],
        ]
    )


def eta_skew_projection(a_matrix: sp.Matrix) -> sp.Matrix:
    return sp.simplify((a_matrix - ETA * a_matrix.T * ETA) / 2)


def boost_vector_from_shift() -> tuple[sp.Expr, sp.Expr, sp.Expr]:
    projection = eta_skew_projection(weakfield_shift_a_matrix())
    return projection[1, 0], projection[2, 0], projection[3, 0]


