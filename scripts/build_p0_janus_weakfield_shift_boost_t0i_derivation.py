def render_markdown(payload: dict) -> str:
    lines = [
        "# P0 Janus Weak-Field Shift Boost T0i Derivation",
        "",
        payload["description"],
        "",
        f"Status: {payload['status']}",
        f"Metric branch with shift: `{payload['metric_branch_with_shift']}`",
        f"Coframe choice: `{payload['coframe_choice']}`",
        f"Derived boost vector: `{payload['derived_boost_vector']}`",
        f"Shift to boost derived: {payload['shift_to_boost_derived']}",
        f"T0i source rows written: {payload['t0i_source_rows_written']}",
        f"Source operator for shift closed: {payload['source_operator_for_shift_closed']}",
        f"Source-derived beta available: {payload['source_derived_beta_available']}",
        f"Pressure/Pi momentum transport closed: {payload['pressure_pi_momentum_transport_closed']}",
        f"Same-L non-comoving branch selected: {payload['same_l_noncomoving_branch_selected']}",
        f"Uses observational fit: {payload['uses_observational_fit']}",
        f"Physics closed: {payload['physics_closed']}",
        f"Prediction ready: {payload['prediction_ready']}",
        "",
        "## Source Rows",
        "",
        "| sector | source row | momentum source | dust linear limit | closed |",
        "|---|---|---|---|---|",
    ]
    for row in payload["source_rows"]:
        lines.append(
            f"| {row['sector']} | `{row['source_row']}` | `{row['momentum_source']}` | "
            f"`{row['dust_linear_limit']}` | {row['closed']} |"
        )
    lines.extend(["", "## Consequences", ""])
    lines.extend(f"- {item}" for item in payload["derived_consequences"])
    lines.extend(["", f"Verdict: {payload['verdict']}", ""])
    return "\n".join(lines)


def write_reports(report_path: Path = REPORT_PATH, json_path: Path = JSON_PATH) -> dict:
    report_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.parent.mkdir(parents=True, exist_ok=True)
    payload = build_payload()
    json_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    report_path.write_text(render_markdown(payload), encoding="utf-8")
    return payload


def main() -> None:
    write_reports()
    print(f"Wrote {REPORT_PATH}")
    print(f"Wrote {JSON_PATH}")


if __name__ == "__main__":
    main()
